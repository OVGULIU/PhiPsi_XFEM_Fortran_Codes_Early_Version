      subroutine Vector_belongs_Matrix_Is_Dou(m,n,Matrix,Vector,
     &                                    Location,Yes)   
C     check if vector belongs to a matrix 
          
      implicit none
      integer i,m,n                
      double precision Matrix(m,n)
      double precision Vector(n),Vector_tem(n)
      integer,intent(out)::Location
      logical,intent(out)::Yes
      
      Yes = .False.
      do i=1,m
          Vector_tem = Matrix(i,:)
          if ((MaxVal(Vector_tem-Vector).eq.0.0D0).and.
     &        (MinVal(Vector_tem-Vector).eq.0.0D0)) then
              Yes = .True.
              Location = i
              exit
          end if
      end do
      
      return 
      end subroutine Vector_belongs_Matrix_Is_Dou
    


