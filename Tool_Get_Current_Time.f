      subroutine Tool_Get_Current_Time(c_data,date_time,millisecond)
      !get current time.
      
      implicit none
      
      integer date_time(8),millisecond
      character*10 b(3),c_data
      
      call date_and_time(b(1), b(2), b(3), date_time)
      
      millisecond = date_time(8)               +    !毫秒
     &              date_time(7)*1000          +    !秒
     &              date_time(6)*60*1000       +    !分钟
     &              date_time(5)*60*60*1000         !小时
     
      c_data= b(1)    !日期

      return 
      end SUBROUTINE Tool_Get_Current_Time                          
